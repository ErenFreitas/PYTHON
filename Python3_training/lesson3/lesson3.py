# Converting integer to float
integer_num = 5
float_num = float(integer_num)
print("Integer " + str(integer_num) + " converted to float: " + str(float_num))

# Converting float to integer
float_num = 3.14
integer_num = int(float_num)
print("Float " + str(float_num) + " converted to integer: " + str(integer_num))



